package Zadania4.Zad5;

/**
 * 5. Sprawdź czy napis "Ala ma kota" zaczyna się od słowa "Ala" (startsWith()) i kończy słowem "kota" (endsWith)
 */
public class StringStartWitch {
    public static void main(String[] args) {
        String zdanie = "Ala ma kota";
        System.out.println (zdanie.startsWith ("Ala"));
        System.out.println (zdanie.endsWith ("kota"));
    }
}
